// 'traverse' function that was given as part of the problem
// Count value is passed by reference
// v1.0
void traverse(int node, int* count) 
{
   int i;
   // graph is acyclic so the first edge is always to a higher numbered node.
   for (i = node+1; i < SIZE; i++) 
      {
      // printf("graph[%d][%d] = %d\n", node, i, graph[node][i]);
      *count = *count + graph[node][i];
      if (!visited[i]) 
         {
         visited[i] = 1;
         traverse(i, count);
         }
      }
}

// Modified 'traverse' function, which traverses the function and returns back the count as value
// v2.0
int traverseModified(int node) 
{
   int i, count = 0;
   // graph is acyclic so the first edge is always to a higher numbered node.
   for (i = node+1; i < SIZE; i++) 
      {
      // printf("graph[%d][%d] == %d\n", node, i, graph[node][i]);
        if (!visited[i]) 
         {
         visited[i] = 1;
         count = traverseModified(i);
         }
      count = count + graph[node][i];
      }
    return count;
}

// Function used by the openMP 'task' and 'parallel for' constucts to traverse the graph and return the count
// Each tread starts off with a node and traverses its next neighbouring higher node if it is not visited yet by other thread 
int traverseParallel(int node, int id) 
{
   int i, count = 0, traverse_flag = 0;

   // graph is acyclic so the first edge is always to a higher numbered node.
   for (i = node+1; i < SIZE; i++) 
      {
  // printf("[%d] graph[%d][%d] == %d\n", id, node, i, graph[node][i]);
      #pragma omp critical
      {
        if (!visited[i]) 
         {
          visited[i] = 1;
          traverse_flag = 1;
         }
         else 
          traverse_flag = 0;
      }
      if (traverse_flag == 1){
        traverse_flag = 0;
        count += traverseParallel(i, id);
      }
      count += graph[node][i];
      }
    return count;
}

